                            <li><img src="img/Predavanje-1/Bash-Command.png"></li>
                            <li>3.3. Komande koje smo koristili + objašnjenje svake</li>
                            <li>cd ~ (Home Directory)</li>
                            <li>ls (Lista Datoteka)</li>
                            <li>mkdir Code (napraviti datoteku sa imenom "Code")</li>
                            <li>git clone https://github.com/algebrateam/phpdev2017.git (Klonirati repository sa "vašeg git-a")</li>
                            <li>git remote add upstream https://github.com/algebrateam/phpdev2017.git (dodati remote upstream link od glavnog algebrateam master-a)</li>
                            <li>git fetch upstream (Fetchati upstream koji smo dodali)</li>
                            <li>git pull upstream master(Pullati upstream sa mastera na svoj lokalni repository te mergati + updetati fileove)</li>
                            <li>ukoliko ovo prvi puta radite morati će te se logiratu u bash preko navedenih komandi</li>
                            <li>git config --global user.email "vašemail@email.com"</li>
                            <li>git config --global user.name "Vaš User ID/Name"</li>
                            <li>git pull upstream master (ukoliko ste se ulogirali na pravi način moći ćete da pullate upstream sa mastera</li>
                            <li>Ukoliko vam dođe da unesete message za commit upiši te vaš komentar te nakon toga pritisnite "ESC" i onda upišite :wq te pritisnite enter</li>
                            <li>clear (obrišite sve commande koje ste koristili te će prozor biti kao nov) </li><br>
