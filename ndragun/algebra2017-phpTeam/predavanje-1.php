                            <li>1.1. Instalirati Git Bash (Windows 7/8.1) ili PowerShell (Windows 10): <a href="https://git-scm.com/downloads" target="_blank">Klinki ovdje!</a></li>
                            <li><img src="img/Predavanje-1/GitBash-Download.png"></li>
                            <li>2. Instalirati Notepad++ radi lakšeg editovanja fileova: <a href="https://notepad-plus-plus.org/download/v7.3.3.html" target="_blank">Klinki ovdje!</a></li>
